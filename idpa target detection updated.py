def deskew_if_needed(img, angle_threshold=5):
    h, w  = img.shape[:2]
    gray  = cv.cvtColor(img, cv.COLOR_BGR2GRAY)
    blur  = cv.GaussianBlur(gray, (5, 5), 0)
    block = int(w * 0.02)
    block = block if block % 2 == 1 else block + 1
    block = max(block, 11)

    def get_thresh(image):
        g = cv.cvtColor(image, cv.COLOR_BGR2GRAY)
        b = cv.GaussianBlur(g, (5, 5), 0)
        bk = int(image.shape[1] * 0.02)
        bk = bk if bk % 2 == 1 else bk + 1
        bk = max(bk, 11)
        t  = cv.adaptiveThreshold(b, 255, cv.ADAPTIVE_THRESH_GAUSSIAN_C, cv.THRESH_BINARY_INV, bk, 3)
        return cv.morphologyEx(t, cv.MORPH_CLOSE, np.ones((3,3), np.uint8))

    def find_torso_polygon(image):
        ih, iw = image.shape[:2]
        t      = get_thresh(image)
        conts, _ = cv.findContours(t, cv.RETR_TREE, cv.CHAIN_APPROX_SIMPLE)
        candidates = []
        for cnt in conts:
            area = cv.contourArea(cnt)
            if area < ih * iw * 0.02 or area > ih * iw * 0.80:
                continue
            if len(cnt) < 5:
                continue
            hull_area = cv.contourArea(cv.convexHull(cnt))
            solidity  = area / hull_area if hull_area > 0 else 0
            if solidity < 0.70:
                continue
            eps    = 0.02 * cv.arcLength(cnt, True)
            approx = cv.approxPolyDP(cnt, eps, True)
            n      = len(approx)
            if n < 5 or n > 16:
                continue
            x, y, bw, bh = cv.boundingRect(cnt)
            if bw / bh < 0.25 or bw / bh > 2.0:
                continue
            if x < 5 and y < 5 and bw > iw * 0.9:
                continue
            candidates.append((cnt, approx, area, (x, y, bw, bh)))
        if not candidates:
            return None, None, None
        candidates.sort(key=lambda c: c[2], reverse=True)
        cnt, approx, area, bbox = candidates[0]
        return cnt, approx, bbox

    corrected = img.copy()

    # ════════════════════════════════════════════════
    # STAGE A — ROTATION
    # ════════════════════════════════════════════════
    cnt, approx, bbox = find_torso_polygon(corrected)

    if cnt is None:
        print("  Deskew: no polygon found, skipping.")
        return corrected

    pts = approx.reshape(-1, 2).astype(np.float32)
    edge_angles = []
    for i in range(len(pts)):
        p1, p2 = pts[i], pts[(i+1) % len(pts)]
        dx, dy = p2[0]-p1[0], p2[1]-p1[1]
        edge_angles.append((np.sqrt(dx**2+dy**2), np.degrees(np.arctan2(dy, dx))))
    edge_angles.sort(reverse=True)
    norm = edge_angles[0][1] % 180
    if norm > 90: norm -= 180
    rot = norm if abs(norm) <= 45 else (norm-90 if norm > 0 else norm+90)

    print(f"  Deskew: rotation={rot:.1f}°", end="")

    if abs(rot) >= angle_threshold:
        ih, iw = corrected.shape[:2]
        M         = cv.getRotationMatrix2D((iw/2, ih/2), rot, 1.0)
        corrected = cv.warpAffine(corrected, M, (iw, ih),
                                  flags=cv.INTER_CUBIC,
                                  borderMode=cv.BORDER_REPLICATE)
        print(f" → rotated", end="")

    # ════════════════════════════════════════════════
    # STAGE B — AUTO ZOOM (before perspective)
    # ════════════════════════════════════════════════
    res_enhancer_needed = False
    ih, iw     = corrected.shape[:2]
    cnt_z, _, bbox_z = find_torso_polygon(corrected)

    if bbox_z is not None:
        cx, cy, cbw, cbh = bbox_z
        area_ratio = (cbw * cbh) / (iw * ih)
        print(f" | fill={area_ratio:.2f}", end="")

        if area_ratio < 0.3:
            SCALE     = 1.5
            new_w     = int(cbw * SCALE)
            new_h     = int(cbh * SCALE)

            pc_x      = cx + cbw // 2
            pc_y      = cy + cbh // 2
            PAD       = int(max(new_w, new_h) * 0.08)

            x1 = max(0,  pc_x - new_w // 2 - PAD)
            y1 = max(0,  pc_y - new_h // 2 - PAD)
            x2 = min(iw, pc_x + new_w // 2 + PAD)
            y2 = min(ih, pc_y + new_h // 2 + PAD)

            corrected = corrected[y1:y2, x1:x2]
            print(f" → zoomed to {x2-x1}×{y2-y1}", end="")
