    # slightly more sensitive, helping it catch faint lines on dark black targets.
    thresh_clean = cv.adaptiveThreshold(
        blur, 255, cv.ADAPTIVE_THRESH_GAUSSIAN_C,
        cv.THRESH_BINARY_INV, block, 2
    )

    # 3. Light Cleanup
    # Shrunk the kernel to (2, 2). A (3, 3) kernel was too thick and was causing
    # nearby lines (like the head and torso) to accidentally merge together.
    kernel = cv.getStructuringElement(cv.MORPH_RECT, (2, 2))
    thresh_clean = cv.morphologyEx(thresh_clean, cv.MORPH_CLOSE, kernel)

    # 4. Find Contours
