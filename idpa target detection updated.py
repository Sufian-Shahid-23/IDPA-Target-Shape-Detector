# CHECKER — decide if a quad is skewed enough to need correction
# =====================================================================
def quad_needs_perspective_fix(corners, angle_tolerance_deg=4.0, ratio_tolerance=0.04):
    """
    corners: (4,2) array TL,TR,BR,BL
    Returns (needs_fix: bool, score: float) where score is a single
    'how skewed' number (0 = perfect rectangle) you can also use for
    convergence checks across iterations.
    """
    TL, TR, BR, BL = corners

    top    = TR - TL
    bottom = BR - BL
    left   = BL - TL
    right  = BR - TR

    top_len, bottom_len = np.linalg.norm(top), np.linalg.norm(bottom)
    left_len, right_len = np.linalg.norm(left), np.linalg.norm(right)

    # opposite-side length symmetry (1.0 = perfectly equal)
    w_ratio = min(top_len, bottom_len) / max(top_len, bottom_len) if max(top_len, bottom_len) > 0 else 1.0
    h_ratio = min(left_len, right_len) / max(left_len, right_len) if max(left_len, right_len) > 0 else 1.0

    # corner angle deviation from 90°
    def angle_at(p_prev, p_curr, p_next):
        v1 = p_prev - p_curr
        v2 = p_next - p_curr
        cos_a = np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2) + 1e-9)
        return np.degrees(np.arccos(np.clip(cos_a, -1.0, 1.0)))

    angles = [
        angle_at(BL, TL, TR),
        angle_at(TL, TR, BR),
        angle_at(TR, BR, BL),
        angle_at(BR, BL, TL),
    ]
    max_angle_dev = max(abs(a - 90) for a in angles)

    ratio_dev = max(1 - w_ratio, 1 - h_ratio)

    needs_fix = (max_angle_dev > angle_tolerance_deg) or (ratio_dev > ratio_tolerance)

    # combined score, useful for convergence comparisons across loop iterations
    score = max_angle_dev + ratio_dev * 100

    return needs_fix, score


