def get_octagon_corners(poly, img_shape, debug=False):
    pts = poly.reshape(-1, 2).astype(np.float64)
    n = len(pts)

    if n != 8:
        raise ValueError(f"Expected exactly 8 vertices for alternating side/cut structure, got {n}")

    edge_lengths = []
    for i in range(8):
        p1, p2 = pts[i], pts[(i + 1) % 8]
        edge_lengths.append(np.linalg.norm(p2 - p1))

    sum_even = sum(edge_lengths[0::2])
    sum_odd  = sum(edge_lengths[1::2])
    side_start = 0 if sum_even > sum_odd else 1
    side_edge_idxs = [(side_start + 2*k) % 8 for k in range(4)]

    if debug:
        print(f"Edge lengths (cyclic): {[round(l,1) for l in edge_lengths]}")
        print(f"side edges at idx {side_edge_idxs}")

    sides = []
    for i in side_edge_idxs:
        p1, p2 = pts[i], pts[(i + 1) % 8]
        mid = (p1 + p2) / 2
        sides.append({"p1": p1, "p2": p2, "mid": mid})

    sides_by_y = sorted(sides, key=lambda s: s["mid"][1])
    top_side    = sides_by_y[0]
    bottom_side = sides_by_y[-1]
    remaining   = sides_by_y[1:-1]

    remaining_by_x = sorted(remaining, key=lambda s: s["mid"][0])
    left_side  = remaining_by_x[0]
    right_side = remaining_by_x[-1]

    classified = {
        "top":    (top_side["p1"], top_side["p2"]),
        "bottom": (bottom_side["p1"], bottom_side["p2"]),
        "left":   (left_side["p1"], left_side["p2"]),
        "right":  (right_side["p1"], right_side["p2"]),
    }

    if debug:
        for name in ["top", "right", "bottom", "left"]:
            print(f"  {name}: mid={classified[name][0].round(1)}->{classified[name][1].round(1)}")

    def line_from_pts(p1, p2):
        x1, y1 = p1; x2, y2 = p2
        a = y2 - y1; b = x1 - x2
        c = -(a*x1 + b*y1)
        return a, b, c

    def intersect(l1, l2):
        a1,b1,c1 = l1; a2,b2,c2 = l2
        det = a1*b2 - a2*b1
        if abs(det) < 1e-9:
            return None
        return np.array([(b1*c2-b2*c1)/det, (a2*c1-a1*c2)/det])

    L_top, L_bottom = line_from_pts(*classified["top"]), line_from_pts(*classified["bottom"])
    L_left, L_right = line_from_pts(*classified["left"]), line_from_pts(*classified["right"])

    TL, TR = intersect(L_top, L_left), intersect(L_top, L_right)
    BR, BL = intersect(L_bottom, L_right), intersect(L_bottom, L_left)

    if any(p is None for p in (TL, TR, BR, BL)):
        raise ValueError("Failed to intersect one or more side pairs (near-parallel lines).")

    if debug:
        print("Corners (TL,TR,BR,BL):", TL, TR, BR, BL)

    return np.array([TL, TR, BR, BL], dtype=np.float32)


def expand_quad_to_fit_contour(quad_corners, target_points, padding_ratio=0.0, max_scale=2.5):
    """
    Same as before, but now caps the scale factor — if the required
    expansion exceeds max_scale, it's almost certainly being driven by
    an outlier point (wall edge, shadow, tape) rather than the real
    torso silhouette, so we clip rather than blow up the canvas.
    """
    quad = np.asarray(quad_corners, dtype=np.float64)
    pts  = np.asarray(target_points, dtype=np.float64).reshape(-1, 2)

    C = quad.mean(axis=0)
    edges = [(quad[0], quad[1]), (quad[1], quad[2]), (quad[2], quad[3]), (quad[3], quad[0])]

    required_k = 1.0
    for p1, p2 in edges:
        d = p2 - p1
        normal = np.array([d[1], -d[0]])
        normal = normal / (np.linalg.norm(normal) + 1e-12)
        if np.dot(normal, C - p1) > 0:
            normal = -normal
        m = -np.dot(normal, C - p1)
        if m < 1e-9:
            continue
        g_pts = (pts - p1) @ normal
        k_needed = 1.0 + g_pts / m
        required_k = max(required_k, np.max(k_needed))

    k_final = required_k * (1.0 + padding_ratio)

    # ── SAFETY CLAMP ──────────────────────────────────────────────
    if k_final > max_scale:
        print(f"  [warn] expansion scale {k_final:.2f} exceeds max_scale={max_scale}, "
              f"clamping (likely outlier point in torso contour)")
        k_final = max_scale
    # ──────────────────────────────────────────────────────────────

    expanded = C + k_final * (quad - C)
    return expanded.astype(np.float32), k_final


def warp_from_corners(raw_img, src_corners, out_size=None):
    h, w = raw_img.shape[:2]

    if out_size is None:
        width_top    = np.linalg.norm(src_corners[1] - src_corners[0])
        width_bottom = np.linalg.norm(src_corners[2] - src_corners[3])
        height_left  = np.linalg.norm(src_corners[3] - src_corners[0])
        height_right = np.linalg.norm(src_corners[2] - src_corners[1])
        out_w = int(max(width_top, width_bottom))
        out_h = int(max(height_left, height_right))
    else:
        out_w, out_h = out_size

    dst_corners = np.array([
        [0,       0],
        [out_w-1, 0],
        [out_w-1, out_h-1],
        [0,       out_h-1]
    ], dtype=np.float32)

    M = cv.getPerspectiveTransform(src_corners.astype(np.float32), dst_corners)
    warped = cv.warpPerspective(raw_img, M, (out_w, out_h))
    return warped, M
